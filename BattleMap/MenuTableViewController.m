#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (indexPath.section == 0) {
        if (indexPath.row == 0) {
            [self performSegueWithIdentifier:@"showScanner" sender:self];
        }
    }
    else if (indexPath.section == 1) {
            
        if (indexPath.row == 0)
        {
            [self performSegueWithIdentifier:@"createNewGame" sender:self];
        }
        else
        {
            [self performSegueWithIdentifier:@"showGame" sender:self];
        }        
    }
    else if (indexPath.section == 2) {
        [self performSegueWithIdentifier:@"showOldGame" sender:self];
    }
   
}

